def chunk_text(text, metadata, chunk_size=2000, overlap=200):
    import hashlib
    separators = ["\n\n", "\n", ". ", " "]
    chunks = _split(text, separators, chunk_size, overlap)
    result = []
    for i, piece in enumerate(chunks):
        piece = piece.strip()
        if len(piece) < 50:
            continue
        cid = hashlib.md5(piece.encode()).hexdigest()
        result.append({"id": cid, "text": piece, "metadata": {**metadata, "chunk_index": i, "company": "ГК Проект"}})
    return result


def _split(text, seps, size, overlap):
    if len(text) <= size:
        return [text]
    sep = seps[0] if seps else " "
    parts = text.split(sep)
    chunks, cur = [], ""
    for p in parts:
        cand = cur + sep + p if cur else p
        if len(cand) > size and cur:
            chunks.append(cur)
            ov = cur[-overlap:] if overlap else ""
            cur = ov + sep + p if ov else p
        else:
            cur = cand
    if cur:
        chunks.append(cur)
    if len(seps) > 1:
        refined = []
        for c in chunks:
            if len(c) > size:
                refined.extend(_split(c, seps[1:], size, overlap))
            else:
                refined.append(c)
        return refined
    return chunks


async def main():
    import chromadb
    from openai import OpenAI

    api_key = os.getenv("OPENAI_API_KEY", "")
    if not api_key:
        logger.error("OPENAI_API_KEY not set!")
        sys.exit(1)

    RAW_DIR.mkdir(parents=True, exist_ok=True)
    for name, page in PAGES.items():
        fpath = RAW_DIR / f"{name}.md"
        fpath.write_text(page["content"], encoding="utf-8")
        logger.info("Saved: %s (%d chars)", fpath.name, len(page["content"]))

    openai_client = OpenAI(api_key=api_key)
    chroma_client = chromadb.PersistentClient(path=CHROMA_DATA_DIR)
    collection = chroma_client.get_or_create_collection(
        name="ai_sale_knowledge", metadata={"hnsw:space": "cosine"}
    )
    before = collection.count()
    logger.info("ChromaDB before: %d documents", before)

    all_chunks = []
    for name, page in PAGES.items():
        meta = {"url": page["url"], "title": page["title"], "category": page["category"], "source": page["url"]}
        chunks = chunk_text(page["content"], meta)
        all_chunks.extend(chunks)
        logger.info("  %s → %d chunks", page["title"], len(chunks))

    logger.info("Total chunks to load: %d", len(all_chunks))

    loop = asyncio.get_running_loop()
    batch_size = 50
    for i in range(0, len(all_chunks), batch_size):
        batch = all_chunks[i:i + batch_size]
        texts = [c["text"] for c in batch]
        ids = [c["id"] for c in batch]
        metadatas = [c["metadata"] for c in batch]

        resp = await loop.run_in_executor(
            None,
            lambda t=texts: openai_client.embeddings.create(model="text-embedding-3-small", input=t),
        )
        embeddings = [item.embedding for item in resp.data]

        await loop.run_in_executor(
            None,
            lambda: collection.upsert(ids=ids, documents=texts, embeddings=embeddings, metadatas=metadatas),
        )
        logger.info("  Loaded batch %d–%d / %d", i + 1, i + len(batch), len(all_chunks))

    after = collection.count()
    logger.info("ChromaDB after: %d documents (+%d new)", after, after - before)
    logger.info("DONE!")


if __name__ == "__main__":
    asyncio.run(main())
